#include "fdf.h"

void	fdf_reset_color(t_env *env)
{
	HUE = 0x00FFFFFF;
	C_A = 0;
	C_R = 255;
	C_G = 255;
	C_B = 255;
}

int		fdf_color(int a, int r, int g, int b)
{
	int color;

	if (a > 255 || r > 255 || g > 255 || b > 255)
	{
		a = (a > 255) ? 255 : a;
		r = (r > 255) ? 255 : r;
		g = (g > 255) ? 255 : g;
		b = (b > 255) ? 255 : b;
	}
	if (a < 0 || r < 0 || g < 0 || b < 0)
		color = 0x00000000;
	else
		color = (a * 256 * 256 * 256) + (r * 256 * 256) + (g * 256) + b;
	return (color);
}
